
public class StackEmptyException extends RuntimeException {

	public StackEmptyException() {// constructor
		super();
	}// end

	public StackEmptyException(String message) {// constructor
		super(message);
	}// end

	public StackEmptyException(String message, Throwable cause) {// constructor
		super(message, cause);
	}// end

}// end StackEmptyException
